// for loop //

// i++ => post increment 
for(let i=0; i<10; i++){
    console.log("Hello"); // print hello 10 times --> it will print the value until it saticfies the condition i<10  
}

// i-- => psot decriment
for(let i=10; i>-1; i--){
    console.log(i); // print numbers in revers order   
}

